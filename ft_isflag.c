#include "printf.h"

int		ft_isflag(char **str, t_flags *flags, int j, int l)
{
	if (l > 0)
	{
		if (str[j][l] == '-' || (str[j][l] == '0' && str[j][l - 1] != '-' &&
		str[j][l + 1] != '-' && str[j][l - 1] != '.' && str[j][l - 1] == '%'))
		{
			flags->flag = str[j][l];
			return (1);
		}
	}
	return (-1);
}
